package edu.itstep.firstproject;

public class Main9 {
    public static void main(String[] args) {
        // цикли
        // for
        // fori

        for (int i = 0; i < 5; i++) {
            System.out.println("Hello");
        }


        int count = 1;
        while (count <= 3){
            System.out.println("work");
            count++;
        }

        do{
            System.out.println("work");
        }while (count++ <= 3);

        //continue; - на початок циклу
        //break; - вийти з циклу

    }
}
